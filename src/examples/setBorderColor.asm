; How to set the border color
; ==========================================================

; ----------------------------------------------------------
; Labels
; ----------------------------------------------------------

border = $d020

; ----------------------------------------------------------
; Main Loop
; ----------------------------------------------------------

        *=$02a7                 ; sys 679

loop    lda #$04                ; set border color to purple
        sta border
        jmp loop